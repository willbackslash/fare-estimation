import pytest
from clean_records import perform_clean_data
from domain.models import RideRecord
from pydantic import ValidationError

from utils import records_belongs_same_route


def test_given_a_valid_row_it_instantiates_a_valid_ride_record():
    row = "1,37.966660,23.728308,1405594957"
    record = RideRecord(*row.strip().split(","))
    assert record.ride_id == "1"
    assert record.latitude == 37.966660
    assert record.longitude == 23.728308
    assert record.timestamp == 1405594957


def test_given_an_invalid_row_it_raises_an_exception():
    row = "1,a,23.728308,1405594957"
    with pytest.raises(ValidationError):
        RideRecord(*row.strip().split(","))


def test_given_records_of_two_paths_they_not_belongs_same_route():
    row1 = "1,37.966660,23.728308,1405594957"
    row2 = "2,37.966660,23.728308,1405594958"
    record1 = RideRecord(*row1.strip().split(","))
    record2 = RideRecord(*row2.strip().split(","))
    assert not records_belongs_same_route(record1, record2)


def test_it_removes_invalid_inputs():
    records = [
        "1,,23.728308,1405594957",
        "1,37.966625,23.728263,1405594974",
        "1,WRONG,DATA,1405594966",
        "1,37.966613,23.728375,1405594984",
        "1,37.966203,23.728597,1405594992",
    ]
    res = perform_clean_data(records)
    valid_records = [str(r) for r in res["1"]]
    assert valid_records == [
        "1,37.966625,23.728263,1405594974",
        "1,37.966613,23.728375,1405594984",
        "1,37.966203,23.728597,1405594992",
    ]


def test_it_removes_routes_of_a_single_record():
    records = [
        "1,37.966613,23.728375,1405594984",
        "2,37.966613,23.728375,1405594984",
    ]
    res = perform_clean_data(records)
    assert res == {}


